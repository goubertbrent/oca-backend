#
# network_country_iso --> 2 digits of country code
# number = phone number clean string (e.g. +32475123456)
#
# Reference: http://en.wikipedia.org/wiki/ISO_3166-1_alpha-2
#
# Android: retrieve from TelephonyManager using getNetworkCountryIso (e.g. 'be')
#
def normalize_phone_number(network_country_iso, number):
    if number.startswith('+'):
        return number
    if network_country_iso not in country_code_index:
        return number
    (unused, unused, unused, in_prefix, out_prefix, trunk_prefix) = country_code_index[network_country_iso]
    if (number.startswith('+')):
        return number
    if (number.startswith(out_prefix)):
        if len(number) > 3 + len(out_prefix):
            # TBD: somehow arbitrary check that we want at least 4 digits after the out_prefix
            return '+' + number[len(out_prefix):]
        else:
            return number
    if (number.startswith(trunk_prefix)):
        if ' ' in in_prefix:
            # This is an exception! The country belongs to the NANP
            return '+' + number
        if len(number) > 2 + len(trunk_prefix):
            # TBD: somehow arbitrary check that we want at least 2 digits after the trunk prefix
            return '+' + in_prefix.replace(' ', '') + number[len(trunk_prefix):]
        else:
            return number
    if (trunk_prefix == '-'):
        if len(number) > 3:
            return '+' + in_prefix.replace(' ', '') + number
        else:
            return number
    if (network_country_iso == 'us') and len(number) == 10:
        # assume 10-digit dialing
        # TBD: what about other countries in NANP?
        return '+1' + number
    return number


